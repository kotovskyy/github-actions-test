"""
    Module with functions for basic mathematical operations on two numbers.
"""


def add(a: int, b: int) -> int:
    """
    Return the sum of two given values.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Return the subtraction of two given values.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Return the mutliplication of two given values.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Return the division of two given values.
    """
    return a / b
